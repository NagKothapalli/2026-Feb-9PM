package coreJavaPrograms;


public class GmailWithMainMethod {
	
	//psvm
	public static void main(String[] args) {
		System.out.println("Gmail Testing with Main Method");
		composeAnEmail();
		replyToAnEmail();
		forwardAnEmail();
		deleteEmail();
	}
	
	public static void composeAnEmail() {
		System.out.println("Test Case : Compose an Email");
	}
	
	public static void replyToAnEmail() {
		System.out.println("Test Case : Reply to an Email");
	}
	
	public static void forwardAnEmail() {
		System.out.println("Test Case : Forward an Email");
	}
	
	public static void deleteEmail() {
		System.out.println("Test Case : Delete an Email");
	}

}
